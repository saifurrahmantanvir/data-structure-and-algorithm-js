/* Big-O of Ojects

insertion - O(1)
removal - O(1)
accessing - O(1) [by key]
searching - O(n) [if any key has a certain value]

*/



/* Big-O of Object methods

Object.keys(), Object.values(), Object.entries() - O(n)
hasOwnProperty() - O(1)

*/




/* Big-O of Arrays

insertion - depends
removal - depends
accessing - O(1)
searching - O(n)

*/


/* Big-O of Array methods

push, pop - O(1)
shift, unshift, concat, slice, splice - O(n)
sort - O(nlogn)
forEach, map, filter, reducer - O(n)

*/